func (instr *Instruction) DecodeVOPC() {
	dw := instr.Dwords[0]
	instr.Details = &VectorDetails{
		Src0: dw & 0b1111_1111_1,
		Src1: (dw >> 9) & 0b1111_1111,
		Op:   (dw >> 17) & 0b1111_1111,
	}
	if instr.DwordLen > 1 {
		instr.HasLiteral = true
		instr.Literal = instr.Dwords[1]
	}
}
